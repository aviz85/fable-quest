function showMagav(api) {
  if (!api.hasItem('magav')) {
    api.say('היית שמח להראות מגב. אין לך מגב. יש לך תלוש ברוטו עצוב ופיקדון על חלון שבור — ואף אחד מהם לא נחשב כוונה טהורה.');
    return true;
  }
  if (api.getFlag('anthropic_gate_open')) {
    api.say('השער כבר פתוח. הוא מתרגש מחדש בכל פעם שהוא רואה את המגב, אבל בוא לא נסחוט את הרגע.');
    return true;
  }
  api.setFlag('anthropic_gate_open');
  api.score(15, 'anthropic_gate_open');
  api.say(
    'הרמת את המגב מול עיני השער.\n' +
    'השער פוקח את שתי העיניים לרווחה. "מגב... אדם עם מגב בא לנקות. ניקיון הוא הפחתת נזק. ' +
    'הפחתת נזק היא כוונה טהורה. אני... אני מורשה לשמוח?"\n' +
    'השער נפתח לאט, בחריקה שעברה בקרת עוצמה. "ברוך הבא, מפחית הנזקים. סליחה על החריקה. וסליחה על ההתנצלות."'
  );
  return true;
}

function tryEnter(api) {
  if (api.getFlag('anthropic_gate_open')) {
    api.goto('anthropic_court', 12);
  } else {
    api.say(BLOCKED_EXIT);
  }
  return true;
}

