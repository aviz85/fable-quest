  // חסימת היציאה ימינה עד שהשער נפתח (אם המנוע תומך ב-onExit)
  onExit(dir, api) {
    if (dir === 'right' && !api.getFlag('anthropic_gate_open')) {
      api.say(BLOCKED_EXIT);
      return false;
    }
    return true;
  },
