    const wantsDown =
      raw.includes('למטה') || raw.includes('צומת') || raw.includes('חזור') ||
      raw.includes('החוצה') || /(^|\s)(רד|תרד|צא|תצא)(\s|$)/.test(raw);
    if (wantsDown && (verb === 'לך' || verb === 'חזור' || verb === 'רד' || verb === 'צא')) {
      api.say('אתה חוזר במורד השביל אל הצומת. השער לוחש אחריך: "לך לשלום. ותחזור. אבל בזהירות. אבל תחזור."');
      api.goto('junction', 160);
      return true;
    }

