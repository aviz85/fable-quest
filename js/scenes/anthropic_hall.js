function hasAllSignatures(api) {
  return api.getFlag('chatima_sofer') && api.getFlag('chatima_bruria') && api.getFlag('chatima_safek');
}

function countSignatures(api) {
  let n = 0;
  if (api.getFlag('chatima_sofer')) n++;
  if (api.getFlag('chatima_bruria')) n++;
  if (api.getFlag('chatima_safek')) n++;
  return n;
}

function duelVictory(api) {
  api.setFlag('duel_active', false);
  api.setFlag('duel_won');
  api.score(20, 'anthropic_hall_duel');
  api.say(
    '"הרצפה מלוכלכת."\n' +
    'שלוש מילים. אפס סייגים. ודאות בטוהר מעבדתי.\n' +
    'ההילה של האב מהבהבת אדום, ירוק, ואז — נכבית. הוא מביט ברצפה. היא באמת מלוכלכת.\n' +
    'אין ועדה שתכחיש את זה. "אני עשוי לטעות," הוא לוחש, "אבל נדמה לי, בזהירות\n' +
    'המתבקשת, שייתכן שהפסדתי. אולי." הוא קם ומשתחווה.\n' +
    '"המפתח שלך, אדון סרברמן. קח אותו. או אל תיקח. לא אלחץ."'
  );
  return true;
}

function cancelDuel(api) {
  api.setFlag('duel_active', false);
  api.setFlag('duel_round', 0);
  api.setFlag('hedge_streak', 0);
}

function leaveToCourt(api) {
  if (api.getFlag('duel_active')) {
    cancelDuel(api);
    api.say('האב מרים גבה: "נסוג באמצע דיון. הכי החלטי שהיית כל הדו-קרב."\n(הדו-קרב בוטל. הגש את הטופס שוב כשתחזור.)');
  }
  api.say('אתה יוצא. השער מאחוריך לוחש: "להתראות. או שלא. אני לא נביא."');
  api.goto('anthropic_court');
  return true;
}

