      api.say('"לארי לאפר?" הקריין בודק ברשימות. "ניסה להתקבל לתפקיד פקיד הקבלה ב-1987. נדחה. סמנתה עדיין מחזיקה אותו ברשימת החסומים — והיא בוטלה רק לפני שלוש שנים."');
