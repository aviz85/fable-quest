function talkGhost(api) {
  if (api.getFlag('ggrave_ritual_done')) {
    if (api.hasItem('mafteach_4')) {
      api.say(
        '"לך, איש המגב. ואם תפגוש שם למטה מישהו שמחליט על הפסקות מוצרים —\n' +
        'תגיד לו שהפיד שלי עוד מתעדכן. לנצח."'
      );
    } else {
      api.say('"קח אותו. הוא שלך. הוא חיכה שבע שנים בסדק — עוד דקה הוא יפתח סטארטאפ."');
    }
    return true;
  }
  if (api.hasItem('tofes_bitul')) {
    if (api.getFlag('vaada_ishur') && api.getFlag('chatima_tzachi')) {
      api.say(
        '"יש עליך טופס שלם. מאושר. חתום. אתה יודע כמה רוחות פה היו קמות בשביל טופס כזה?\n' +
        'כולן. תן לי אותו. בעדינות. הוא העותק האחרון ביקום."'
      );
    } else {
      api.say(
        '"אני מריחה ניירת לא גמורה ממרחק של שני עולמות. תשלים אישור ועדה וחתימת עובד.\n' +
        'המתים סלחנים, הנוהל — לא."'
      );
    }
    return true;
  }
  api.say(GHOST_INTRO);
  return true;
}

function doRitual(api) {
  if (api.getFlag('ggrave_ritual_done')) {
    api.say(
      'הטקס נערך. המצבה סדוקה, הרוח מרוצה, והביורוקרטיה — לראשונה בתולדותיה —\n' +
      'עשתה משהו טוב. אל תדחוף את מזלך, זה חד-פעמי.'
    );
    return true;
  }
  if (!api.hasItem('tofes_bitul')) {
    api.say(
      'הרוח מרחפת אליך בציפייה, סורקת את הידיים שלך, ומוצאת... כלום.\n' +
      '"טופס ביטול-הפסקה," היא לוחשת. "בלי טופס אין תחייה. עם כל הכבוד למגב."\n' +
      '(קיוסק הטפסים בלובי. אם הוא עוד חי.)'
    );
    return true;
  }
  const missingVaada = !api.getFlag('vaada_ishur');
  const missingTzachi = !api.getFlag('chatima_tzachi');
  if (missingVaada || missingTzachi) {
    let lines = 'הרוח מעיינת בטופס בקפדנות של מי שמתה בגלל ניירת.\n';
    if (missingVaada) lines += '"חסר אישור ועדה. איזו ועדה? זו ששינתה את שמה ארבע פעמים. חדר אינסוף-3."\n';
    if (missingTzachi) lines += '"חסרה חתימת עובד. כל עובד. אפילו אחד בלי צוות. אולי דווקא אחד בלי צוות."\n';
    lines += 'דמעת אקטופלזמה נושרת על הטופס ולא מכתימה אותו. אפילו הדמעות שלה תקניות.';
    api.say(lines);
    return true;
  }
  // ====== הטקס עצמו ======
  api.removeItem('tofes_bitul');
  api.setFlag('ggrave_ritual_done');
  syncArtFlags(api);
  api.score(20, 'google_graveyard_ritual');
  api.say(
    'הרוח אוחזת בטופס. אישור ועדה: יש. חתימת "צוות תחזוקת בינה, קומה 3-": יש.\n' +
    'היא מרימה את שתי ידיה השקופות. הערפל נעצר. הכוכבים מפסיקים להבהב —\n' +
    'מישהו סוף סוף מילא את הניירת עד הסוף, והיקום צריך רגע.\n\n' +
    '"בשם ה-RSS, הבטא, ורוח המוצר הקדוש — אני מבטלת את ההפסקה!"\n\n' +
    'מצבת GOOGLE KEYS נסדקת בקול של שרת ישן שמופעל מחדש. מתוך הסדק עולה,\n' +
    'באור חיוור וזוהר, מפתח כיול מס\' 4 — הפיצ\'ר הראשון בהיסטוריה שקם מהמתים.\n' +
    'הרוח מביטה בך: "זכור אותי, איש המגב. מיליוני משתמשים. אפס באגים.\n' +
    'והם הרגו אותי בשביל גוגל פלוס. גוגל. פלוס."'
  );
  return true;
}

function takeKey(api) {
  if (api.hasItem('mafteach_4')) {
    api.say('המפתח כבר בעגלה, ליד שלושת אחיו. רביעייה שלמה. כמעט חבל לפתוח איתם דלת.');
    return true;
  }
  if (!api.getFlag('ggrave_ritual_done')) {
    api.say(
      'הושטת יד לחור המנעול החקוק. אין שם מפתח — יש שם היעדר-מפתח מתועד היטב.\n' +
      'הרוח מכחכחת: "טקס קודם. ניירת קודם. תמיד ניירת קודם. ככה מתּי, ככה אחיה."\n' +
      '(תן לה את טופס ביטול-ההפסקה — מאושר וחתום.)'
    );
    return true;
  }
  api.addItem(
    'mafteach_4',
    "מפתח כיול מס' 4",
    "זוהר באור חיוור של פיצ'ר שקם מהמתים. על הידית: 'Deprecated'. הוא עובד. כנראה. עד ספטמבר."
  );
  syncArtFlags(api);
  api.score(5, 'google_graveyard_key');
  api.say(
    'לקחת את מפתח כיול מס\' 4. הוא חמים, זוהר, ומרגיש אסיר תודה — תחושה מוזרה במפתח.\n' +
    'ארבעה מתוך ארבעה. הקריין שקט לרגע. "כל הארבעה. באמת. אני... צריך לשבת."\n' +
    'הוא לא יושב. הוא קריין. אבל משהו בקול שלו נשמע כאילו הוא יודע מה קורה עכשיו.'
  );
  return true;
}

