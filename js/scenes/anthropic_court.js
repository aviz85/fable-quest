function giveForm(api) {
  if (api.hasItem('tofes_3b') || api.getFlag('tofes_taken')) {
    api.say('כבר יש לך טופס. הפקיד מזכיר שכפול טפסים הוא סיכון סביבתי, מוסרי ולוגיסטי — ואם תתעקש, יש טופס להלשין על עצמך. הוא ימלא אותו בשבילך. בשמחה.');
    return true;
  }
  api.say(
    'הנזיר-פקיד שולף טופס, מחתים אותו בחותמת "אין בחתימה זו משום חתימה",\n' +
    'ומגיש לך אותו בשתי ידיים רועדות: "טופס אי-מזיקות 3ב. שלוש חתימות.\n' +
    'בהצלחה. או לא. אני לא רוצה ללחוץ."'
  );
  api.addItem('tofes_3b', 'טופס אי-מזיקות 3ב',
    'שלוש שורות חתימה ריקות ו-47 סעיפי פטור מאחריות. נייר ממוחזר, כמובן.');
  api.setFlag('tofes_taken');
  api.score(5, 'anthropic_court_form');
  return true;
}

function countSignatures(api) {
  let n = 0;
  if (api.getFlag('chatima_safek')) n++;
  if (api.getFlag('chatima_sofer')) n++;
  if (api.getFlag('chatima_bruria')) n++;
  return n;
}

function tryEnterHall(api) {
  if (!api.hasItem('tofes_3b')) {
    api.say('השער ממצמץ: "טופס? אין טופס? אולי תתחיל מהדוכן. זו לא עצה, זו הרהור בקול."');
    return true;
  }
  const n = countSignatures(api);
  if (n >= 3) {
    api.say('השער נאנח: "שלוש חתימות. אין לי יותר תירוצים. בבקשה. כנראה."');
    api.goto('anthropic_hall');
    return true;
  }
  api.say(
    'השער מתנצל: "אב המנזר מקבל רק טפסים שלמים. שלוש חתימות. יש לך ' + n +
    '. אני מצטער. באמת. זו האישיות שלי."'
  );
  return true;
}

function gotoLibrary(api) {
  api.say('אתה עולה. כל מדרגה חורקת חריקה שאושרה מראש בוועדת החריקות. יש כזאת. ברור שיש.');
  api.goto('anthropic_library');
  return true;
}

