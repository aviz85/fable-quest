function tradeTicket(api) {
  if (api.getFlag('gadi_traded')) {
    api.say('גדי כבר קיבל את הכרטיס. הוא מלטף אותו בין שיחות. אל תהרוס לו את הרגע היחיד של שקט.');
    return true;
  }
  if (!api.hasItem('kartis_hagrala')) {
    api.say('אין לך כרטיס הגרלה. יש לך רק חלומות, ועל חלומות גדי לא משלם — יש לו מחסן מלא משלו, ממוינים לפי רבעון.');
    return true;
  }
  api.removeItem('kartis_hagrala');
  api.addItem(
    'tag_tzevet',
    "תג 'צוות אירוע'",
    'שרוך כתום ותמונה של מישהו שהוא בערך אתה. בעולם האירועים — דרכון, ויזה ותעודת יושר.'
  );
  api.setFlag('gadi_traded');
  api.score(15, 'openai_expo_badge');
  api.say(G2A);
  api.say(G2B);
  return true;
}

